#include <stdio.h>
int a[101],n;//定义全局变量，这两个变量需要在子函数中使用 

//快速排序函数 
void quicksort(int left,int right) //左右哨兵是两个整型变量 
{
	int i,j,t,temp;
	if(left>right)
		return;
	
	temp=a[left];//temp中存的就是基准数 ，取得是左哨兵对应的值 
	i=left;
	j=right;
	while(i!=j)
	{
		while(a[j]>=temp&&i<j)//顺序很重要，要先从右往左找 
		{
			j--;
		}
		while(a[i]<=temp&&i<j)// 再从左往右找 
		{
			i++;
		}
		//交换两个数在数组中的位置 
		if(i<j)//当哨兵i和哨兵j没有相遇时 
		{
			t=a[i];
			a[i]=a[j];
			a[j]=t;
		}
	}
	//当左哨兵向右移动撞上右哨兵时，将基准数归位 
	//结果是基准数左侧的数都小于基准数，右侧的数都大于基准数 
	a[left]=a[i]; 
	a[i]=temp;
	
	quicksort(left,i-1);//继续处理左边的，这里是一个递归的过程 
	quicksort(i+1,right);//继续处理右边的，这里是一个递归的过程 
	return;
	
}

int main()
{
	int i,j;
	scanf("%d",&n);//排序数个数 
	for(i=1;i<=n;i++)
	{
		scanf("%d",&a[i]); //待排序数都读入数组中 
	}
	quicksort(1,n);
	//输出排序后的结果 
	for(i=1;i<=n;i++)
	{
		printf("%d",a[i]);
	}
	getchar();getchar();
	return 0;
}
